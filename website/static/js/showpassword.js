  function myFunction1() {
    var x = document.getElementById("passwordw");
    var y = document.getElementById("passwords");
    if (x.type === "password" && y.type === "password") {
      x.type = "text";
      y.type = "text";
    } else {
      x.type = "password";
      y.type = "password";
    }
  }

